<div class="text-lg leading-relaxed lg:text-2xl">
  Activitățile de informare sunt instrumente puternice pentru atingerea
  obiectivelor legate de creșterea conștientizării, educarea tinerilor și
  mobilizarea societății, în vederea construirii unei societăți mai echitabile
  din punct de vedere de gen. Prin informare:
  <ol class="list-decimal list-inside">
    <li>
      Conștientizăm: aducem la cunoștința publicului aspecte și probleme legate
      de inegalitățile de gen și stereotipurile associate, creând astfel
      conștientizarea nevoii de schimbare;
    </li>
    <li>
      Educăm: informarea furnizează cunoștințe și înțelegere asupra conceptelor
      legate de egalitatea de gen. Prin educație, se creează o bază solidă
      pentru schimbarea mentalităților și a comportamentelor discriminatorii;
    </li>
    <li>
      Mobilizăm: prin furnizarea de informații, mobilizăm oamenii să se implice
      în lupta pentru egalitatea de gen și să devină parte activă a procesului
      de schimbare;
    </li>
    <li>
      Sensibilizăm: informarea contribuie la creșterea nivelului de empatie și
      înțelegere față de experiențele altor persoane, în special a femeilor și a
      celor afectați de inegalități de gen, ceea ce poate duce la o mai mare
      susținere și solidaritate în comunitate;
    </li>
    <li>
      Schimbăm politici: prin evidențierea problemelor și furnizarea de date
      relevante, informarea poate influența schimbarea politicilor la nivel
      local sau național în direcția unei societăți mai echitabile;
    </li>
    <li>
      Prevenim discriminarea: informarea ajută la identificarea și prevenirea
      comportamentelor discriminatorii. Persoanele informate sunt mai puțin
      predispuse să accepte sau să perpetueze stereotipuri și discriminări de
      gen.
    </li>
  </ol>
</div>
