<div class="leading-relaxed text-md lg:text-xl">
  Capacitarea femeilor și fetelor este esențială pentru a le oferi șanse egale,
  pentru a le dezvolta abilitățile și pentru a contribui la construirea unei
  societăți mai echitabile. Capacitând fetele și femei, vizăm:
  <ol class="list-decimal list-inside">
    <li>
      Egalitatea de șanse: capacitarea oferă femeilor și fetelor acces la
      educație, resurse și oportunități, contribuind astfel la crearea unei
      societăți în care toți indivizii au șanse egale de a-și atinge
      potențialul;
    </li>
    <li>
      Independența economică: prin dobândirea abilităților și competențelor,
      femeile devin mai independente economic, având posibilitatea să-și
      gestioneze resursele financiare și să contribuie la venitul familiei;
    </li>
    <li>
      Redresarea inegalităților de gen: capacitarea contracarează inegalitățile
      de gen, oferind femeilor instrumente pentru a se ridica împotriva
      discriminării și pentru a-și asuma roluri de lideri în diverse domenii;
    </li>
    <li>
      Sănătate și bunăstare: educația și capacitarea în domeniul sănătății ajută
      femeile să-și gestioneze mai bine propria sănătate și să contribuie la
      îmbunătățirea stării de sănătate a comunităților în care trăiesc;
    </li>
    <li>
      Contribuție la dezvoltarea economică: femeile capabile să lucreze în
      diverse sectoare contribuie la creșterea economică a unei societăți,
      aducând idei inovatoare și perspectivă diversă în forța de muncă;
    </li>
    <li>
      Combaterea violenței de gen: capacitarea oferă femeilor instrumente și
      cunoștințe pentru a se apăra împotriva violenței de gen și pentru a
      contribui la crearea unui mediu sigur;
    </li>
    <li>
      Promovarea leadership-ului feminin: capacitarea sprijină dezvoltarea
      leadership-ului feminin, contribuind astfel la echilibrarea reprezentării
      de gen în poziții de conducere și luarea deciziilor;
    </li>
    <li>
      Implicare civică: femeile și fetele capacitate sunt mai susceptibile să se
      implice în comunitate, să-și exprime opiniile și să participe activ la
      schimbarea socială.
    </li>
  </ol>
  Capacitarea femeilor și fetelor nu doar că le ajută pe ele individual, ci are un
  impact pozitiv asupra întregii societăți, contribuind la construirea unei societăți
  echitabile, incluzive și prospere.
</div>
