<div class="leading-relaxed text-md lg:text-xl">
  Capacitarea femeilor și fetelor este esențială pentru a le oferi șanse egale,
  pentru a le dezvolta abilitățile și pentru a contribui la construirea unei
  societăți mai echitabile. Capacitând fetele și femeile, vizăm:
  <ol class="flex flex-col gap-4 p-2 list-decimal list-inside">
    <li>
      <strong>Egalitatea de șanse</strong>: capacitarea oferă femeilor și
      fetelor acces la educație, resurse și oportunități, contribuind astfel la
      crearea unei societăți în care toți indivizii au șanse egale de a-și
      atinge potențialul;
    </li>
    <li>
      <strong>Independența economică</strong>: prin dobândirea abilităților și
      competențelor, femeile devin mai independente economic, având
      posibilitatea să-și gestioneze resursele financiare și să contribuie la
      venitul familiei;
    </li>
    <li>
      <strong>Redresarea inegalităților de gen</strong>: capacitarea
      contracarează inegalitățile de gen, oferind femeilor instrumente pentru a
      se ridica împotriva discriminării și pentru a-și asuma roluri de lideri în
      diverse domenii;
    </li>
    <li>
      <strong>Sănătate și bunăstare</strong>: educația și capacitarea în
      domeniul sănătății ajută femeile să-și gestioneze mai bine propria
      sănătate și să contribuie la îmbunătățirea stării de sănătate a
      comunităților în care trăiesc;
    </li>
    <li>
      <strong>Contribuție la dezvoltarea economică</strong>: femeile capabile să
      lucreze în diverse sectoare contribuie la creșterea economică a unei
      societăți, aducând idei inovatoare și perspectivă diversă în forța de
      muncă;
    </li>
    <li>
      <strong>Combaterea violenței de gen</strong>: capacitarea oferă femeilor
      instrumente și cunoștințe pentru a se apăra împotriva violenței de gen și
      pentru a contribui la crearea unui mediu sigur;
    </li>
    <li>
      <strong>Promovarea leadership-ului feminin</strong>: capacitarea sprijină
      dezvoltarea leadership-ului feminin, contribuind astfel la echilibrarea
      reprezentării de gen în poziții de conducere și luarea deciziilor;
    </li>
    <li>
      <strong>Implicare civică</strong>: femeile și fetele capacitate sunt mai
      susceptibile să se implice în comunitate, să-și exprime opiniile și să
      participe activ la schimbarea socială.
    </li>
  </ol>
  Capacitarea femeilor și fetelor nu doar că le ajută pe ele individual, ci are un
  impact pozitiv asupra întregii societăți, contribuind la construirea unei societăți
  echitabile, incluzive și prospere.
</div>
